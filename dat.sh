#!/bin/bash

# Not Running
notrunning(){
    read -p "
[1] Start Docker
[2] Exit
-> " notr

    case $notr in
        1)
            systemctl start docker
            menu
            ;;
        2)
            echo -e "+-----------------------------------------------------------------------------------------------------+"
            echo -e "Bye :)"
            echo -e "+-----------------------------------------------------------------------------------------------------+"
            ;;
        *)
            echo -e "\nInvalid Option >:("
            notrunning
            ;;
    esac
}


# Manage Containers
menuopt2(){
    read -p "
[1] Show Containers
[2] Container Shell
[3] Copy a File (Host -> Container)
[4] Copy a File (Container -> Host)
[5] Rename a container
[6] Create an image out of a container
[7] Stop a container
[8] Delete a Container
[9] Delete ALL stoped containers
[10] Exit
[11] Menu
-> " opt2

    case $opt2 in
        1)
            echo -e "\n+-----------------------------------------------------------------------------------------------------+"
            docker ps -a
            echo -e "+-----------------------------------------------------------------------------------------------------+"
            menuopt2
            ;;
        2)
            echo -e "\n+-----------------------------------------------------------------------------------------------------+"
            docker ps -a
            echo -e "+-----------------------------------------------------------------------------------------------------+"
            read -p "CONTAINER ID: " contid
            docker start $contid
            docker exec -it $contid bash
            ;;
        3)
            echo -e "\n+-----------------------------------------------------------------------------------------------------+"
            docker ps -a
            echo -e "+-----------------------------------------------------------------------------------------------------+"
            read -p "CONTAINER ID: " contid
            read -p "Host Path: " hpath
            read -p "Container Path: " cpath
            docker cp $hpath $contid:$cpath
            menuopt2
            ;;
        4) 
            echo -e "\n+-----------------------------------------------------------------------------------------------------+"
            docker ps -a
            echo -e "+-----------------------------------------------------------------------------------------------------+"
            read -p "CONTAINER ID: " contid
            read -p "Container Path: " cpath
            read -p "Host Path: " hpath
            docker cp $contid:$cpath $hpath 
            menuopt2
            ;;
        5)  
            echo -e "\n+-----------------------------------------------------------------------------------------------------+"
            docker ps -a
            echo -e "+-----------------------------------------------------------------------------------------------------+"
            read -p "Container OLD name: " contondname
            read -p "Container NEW name: " contnewname
            docker rename $contondname:$contnewname  
            menuopt2
            ;;
        6)
            echo -e "\n+-----------------------------------------------------------------------------------------------------+"
            docker ps -a
            echo -e "+-----------------------------------------------------------------------------------------------------+"
            read -p "CONTAINER ID: " contid
            docker commit $contid
            menuopt2
            ;;
        7)
            echo -e "\n+-----------------------------------------------------------------------------------------------------+"
            docker ps -a
            echo -e "+-----------------------------------------------------------------------------------------------------+"
            read -p "CONTAINER ID: " contid
            docker stop $contid
            menuopt2
            ;;
        8)
            echo -e "\n+-----------------------------------------------------------------------------------------------------+"
            docker ps -a
            echo -e "+-----------------------------------------------------------------------------------------------------+"
            read -p "CONTAINER ID: " contid
            docker rm -f $contid
            menuopt2
            ;;
        9)
            echo -e "\n+-----------------------------------------------------------------------------------------------------+"
            docker container prune
            menuopt2
            ;;
        10)
            echo -e "+-----------------------------------------------------------------------------------------------------+"
            echo -e "Bye :)"
            echo -e "+-----------------------------------------------------------------------------------------------------+"
            ;;
        11)
            echo -e "+-----------------------------------------------------------------------------------------------------+"
            menu
            ;;
        *)
            echo -e "\nInvalid Option >:("
            menuopt2
            ;;
    esac
}


